public class LatDoWhile {
    public static void main(String[] args) {
        int i = 1; //deklarasi & inisialisasi
        do {
            System.out.println(i + ". halo apa kabar");
            i = i + 1; //i++
        }
        while (i <= 5);
        System.out.println("terima kasih");
    }
}
